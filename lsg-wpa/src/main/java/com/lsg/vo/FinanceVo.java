package com.lsg.vo;

public class FinanceVo {
	
	private String financer;
	
	private String workerUserId;
	
	private String postionId;
	
	private String merchId;
	
	private String postionApplyId;
	
	private String wageAmt;
	
	private String auditStat;
	
	public String getFinancer() {
		return financer;
	}

	public void setFinancer(String financer) {
		this.financer = financer;
	}

	public String getWorkerUserId() {
		return workerUserId;
	}

	public void setWorkerUserId(String workerUserId) {
		this.workerUserId = workerUserId;
	}

	public String getPostionId() {
		return postionId;
	}

	public void setPostionId(String postionId) {
		this.postionId = postionId;
	}

	public String getMerchId() {
		return merchId;
	}

	public void setMerchId(String merchId) {
		this.merchId = merchId;
	}

	public String getPostionApplyId() {
		return postionApplyId;
	}

	public void setPostionApplyId(String postionApplyId) {
		this.postionApplyId = postionApplyId;
	}

	public String getWageAmt() {
		return wageAmt;
	}

	public void setWageAmt(String wageAmt) {
		this.wageAmt = wageAmt;
	}

	public String getAuditStat() {
		return auditStat;
	}

	public void setAuditStat(String auditStat) {
		this.auditStat = auditStat;
	}
	
	

}
